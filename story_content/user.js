function Script1()
{
  var player = GetPlayer();
var email = 'PtCTraining@Nats.co.uk'

var courseTitle = player.GetVar('feedback_courseTitle');
var slideNum = player.GetVar('feedback_slideNum');
var slideTitle = player.GetVar('feedback_slideTitle');

var subject = 'BUG REPORT: ' + courseTitle;
var emailBody = 'I have spotted a bug within the eLearning content above.' + '\n\nThe bug can be found on slide ' + slideNum + ' (' + slideTitle + ')' + '\n\nPlease include additional information below:\n\n';
var mailto_link = 'mailto:' + email + '?subject=' + subject + '&body=' + encodeURIComponent(emailBody);
win = window.open(mailto_link, 'emailWin');
}

